

import java.io.File;



public class GeoTaggedImage {
	
	private int img_ID;
	private String img_Description;
	private double img_latitude;
	private double img_longitude;
	private String img_filePath;
	private String img_placeTaken;
	public int getImg_ID() {
		return img_ID;
	}
	public void setImg_ID(int img_ID) {
		this.img_ID = img_ID;
	}
	public String getImg_Description() {
		return img_Description;
	}
	public void setImg_Description(String img_Description) {
		this.img_Description = img_Description;
	}
	public double getImg_latitude() {
		return img_latitude;
	}
	public void setImg_latitude(double img_latitude) {
		this.img_latitude = img_latitude;
	}
	public double getImg_longitude() {
		return img_longitude;
	}
	public void setImg_longitude(double img_longitude) {
		this.img_longitude = img_longitude;
	}
	public String getImg_filePath() {
		return img_filePath;
	}
	public void setImg_filePath(String img_filePath) {
		this.img_filePath = img_filePath;
	}
	public String getImg_placeTaken() {
		return img_placeTaken;
	}
	public void setImg_placeTaken(String img_placeTaken) {
		this.img_placeTaken = img_placeTaken;
	}
	
	
	
}
